package com.uchump.prime.Metatron.Lib.SimpleHttp;
public class HttpUnknownHostException extends HttpException {

    public HttpUnknownHostException() {
        super();
    }

    public HttpUnknownHostException(String message) {
        super(message);
    }

    public HttpUnknownHostException(String message, Throwable cause) {
        super(message, cause);
    }

    public HttpUnknownHostException(Throwable cause) {
        super(cause);
    }
}